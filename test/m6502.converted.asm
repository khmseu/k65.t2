* 		"FUNDSP" CONTAI THE ADDRES OF THE
* 		FUNCTI ROUTIN IN THE ORDER OF THE
* 		FUNCTI NAMES IN THE CRUNCH LIST.
* 		THE FUNCTI THAT TAKE MORE THAN ONE ARGUME
* 		ARE AT THE END. SEE THE EXPLAN AT "ISFUN".

* 	THE OPERAT LIST
* 		THE "OPTAB" LIST CONTAI AN OPERAT'S PRECEDENCE
* 		FOLLOW BY THE ADDRES OF THE ROUTIN TO PERFOR
* 		THE OPERAT THE INDEX INTO THE
* 		OPERAT LIST IS MADE BY SUBTRA OFF THE CRUNCH VALUE
* 		OF THE LOWEST NUMBER OPERAT THE ORDER
* 		OF OPERAT IN THE CRUNCH LIST AND IN "OPTAB" IS IDENTI
* 		THE PRECED ARE ARBITR EXCEPT FOR THEIR
* 		COMPAR SIZES. NOTE THAT THE PRECED FOR
* 		UNARY OPERAT SUCH AS "NOT" AND NEGATI ARE
* 		SETUP SPECIA WITHOU USING THE LIST.

* 	THE RESERV WORD OR CRUNCH LIST
* 		WHEN A COMMAN OR PROGRA LINE IS TYPED IN
* 		IT IS STORED IN "BUF". AS SOON AS THE WHOLE LINE
* 		HAS BEEN TYPED IN ("INLIN" RETURN) "CRUNCH" IS
* 		CALLED TO CONVER ALL RESERV WORDS TO THEIR
* 		CRUNCH VALUES THIS REDUCE THE SIZE OF THE
* 		PROGRA AND SPEEDS UP EXECUT BY ALLOWI
* 		LIST DISPAT TO PERFOR FUNCTI, STATEM,
* 		AND OPERAT THIS IS BECAUS ALL THE STATEM
* 		NAMES ARE STORED CONSEC IN THE CRUNCH LIST.
* 		WHEN A MATCH IS FOUND BETWEE A STRING
* 		OF CHARAC AND A WORD IN THE CRUNCH LIST
* 		THE ENTIRE TEXT OF THE MATCHE WORD IS TAKEN OUT OF
* 		THE INPUT LINE AND A RESERV WORD TOKEN IS PUT
* 		IN ITS PLACE. A RESERV WORD TOKEN IS ALWAYS EQUAL
* 		TO OCTAL 200 PLUS THE POSITI OF THE MATCHE WORD
* 		IN THE CRUNCH LIST.

* 	STATEM DISPAT ADDRES
* 		WHEN A STATEM IS TO BE EXECUT, THE FIRST
* 		CHARAC OF THE STATEM IS EXAMIN
* 		TO SEE IF IT IS LESS THAN THE RESERV
* 		WORD TOKEN FOR THE LOWEST NUMBER STATEM NAME.
* 		IF SO, THE "LET" CODE IS CALLED TO
* 		TREAT THE STATEM AS AN ASSIGN STATEM
* 		OTHERW A CHECK IS MADE TO MAKE SURE THE
* 		RESERV WORD NUMBER IS NOT TOO LARGE TO BE A
* 		STATEM TYPE NUMBER IF NOT THE ADDRES
* 		TO DISPAT TO IS FETCHE FROM "STMDSP" (THE STATEM
* 		DISPAT LIST) USING THE RESERV WORD
* 		NUMBER FOR THE STATEM TO CALCUL AN INDEX INTO
* 		THE LIST.

* 	ERROR MESSAG
* 		WHEN AN ERROR CONDIT IS DETECT,
* 		[ACCX] MUST BE SET UP TO INDICA WHICH ERROR
* 		MESSAG IS APPROP AND A BRANCH MUST BE MADE
* 		TO "ERROR". THE STACK WILL BE RESET AND ALL
* 		PROGRA CONTEX WILL BE LOST. VARIAB
* 		VALUES AND THE ACTUAL PROGRA REMAIN INTACT
* 		ONLY THE VALUE OF [ACCX] IS IMPORT WHEN
* 		THE BRANCH IS MADE TO ERROR. [ACCX] IS USED AS AN
* 		INDEX INTO "ERRTAB" WHICH GIVES THE TWO
* 		CHARAC ERROR MESSAG THAT WILL BE PRINTE ON THE
* 		USER'S TERMINAL.


* 	TEXTUA MESSAG
* 		CONSTA MESSAG ARE STORED HERE. UNLESS
* 		THE CODE TO CHECK IF A STRING MUST BE COPIED
* 		IS CHANGE THESE STRING MUST BE STORED ABOVE
* 		PAGE ZERO, OR ELSE THEY WILL BE COPIED BEFORE
* 		THEY ARE PRINTE

* 	FNDFOR
* 		MOST SMALL ROUTIN ARE FAIRLY SIMPLE
* 		AND ARE DOCUME IN PLACE. "FNDFOR" IS
* 		USED FOR FINDIN "FOR" ENTRIE ON
* 		THE STACK. WHENEV A "FOR" IS EXECUT, A
* 		16-BYTE ENTRY IS PUSHED ONTO THE STACK.
* 		BEFORE THIS IS DONE, HOWEVE, A CHECK
* 		MUST BE MADE TO SEE IF THERE
* 		ARE ANY "FOR" ENTRIE ALREAD ON THE STACK
* 		FOR THE SAME LOOP VARIAB IF SO, THAT "FOR" ENTRY
* 		AND ALL OTHER "FOR" ENTRIE THAT WERE MADE AFTER IT
* 		ARE ELIMIN FROM THE STACK. THIS IS SO A
* 		PROGRA THAT JUMPS OUT OF THE MIDDLE
* 		OF A "FOR" LOOP AND THEN RESTAR THE LOOP AGAIN
* 		AND AGAIN WON'T USE UP 18 BYTES OF STACK
* 		SPACE EVERY TIME. THE "NEXT" CODE ALSO
* 		CALLS "FNDFOR" TO SEARCH FOR A "FOR" ENTRY WITH
* 		THE LOOP VARIAB IN
* 		THE "NEXT". AT WHATEV POINT A MATCH IS FOUND
* 		THE STACK IS RESET. IF NO MATCH IS FOUND A
* 		"NEXT WITHOUT FOR"  ERROR OCCURS GOSUB EXECUT
* 		ALSO PUTS A 5-BYTE ENTRY ON STACK.
* 		WHEN A RETURN IS EXECUT "FNDFOR" IS
* 		CALLED WITH A VARIAB POINTE THAT CAN'T
* 		BE MATCHE WHEN "FNDFOR" HAS RUN
* 		THROUG ALL THE "FOR" ENTRIE ON THE STACK
* 		IT RETURN AND THE RETURN CODE MAKES
* 		SURE THE ENTRY THAT WAS STOPPE
* 		ON IS A GOSUB ENTRY. THIS ASSURE THAT
* 		IF YOU GOSUB TO A SECTIO OF CODE
* 		IN WHICH A FOR LOOP IS ENTERE BUT NEVER
* 		EXITED THE RETURN WILL STILL BE
* 		ABLE TO FIND THE MOST RECENT
* 		GOSUB ENTRY. THE "RETURN" CODE ELIMIN THE
* 		"GOSUB" ENTRY AND ALL "FOR" ENTRIE MADE AFTER
* 		THE GOSUB ENTRY.

* 	NON-RUNTIM STUFF
* 		THE CODE TO INPUT A LINE, CRUNCH IT, GIVE ERRORS,
