 JSR	FMAPTR		;FORM ARYPNT.
	JSR	REASON
	LDA #0
	TAY
	STA	CURTOL+1
.if (ADDPRC) == 0
	LDX #4
	.endif
.if (ADDPRC) != 0
	LDX #5
	.endif
	LDA	VARNAM		;THIS CODE ONLY WORKS FOR INTPRC=1
	STA (LOWTR),Y		;IF ADDPRC=1.
.if (ADDPRC) != 0
	BPL	NOTFLT
	DEX
	.endif
