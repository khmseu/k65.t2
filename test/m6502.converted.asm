* 		SPACE IS ALLOCA FOR VARIAB AS THEY ARE
* 		ENCOUN THUS "DIM" STATEM MUST BE
* 		EXECUT TO HAVE EFFECT 6 BYTES ARE ALLOCA
* 		FOR EACH SIMPLE VARIAB, WHETHE IT IS A STRING,
