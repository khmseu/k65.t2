* 		THE VALUE IN THE FLOATI ACCUMU (FAC).
* 		"TXTPTR" IS RETURN POINTI TO THE FIRST CHARAC
* 		THAT COULD NOT BE INTERP AS PART OF THE
* 		FORMUL THE ALGORI USES THE STACK
* 		TO STORE TEMPOR RESULT:

* 			0. PUT A DUMMY PRECED OF ZERO ON
* 				THE STACK.
* 			1. READ LEXEME (CONSTA,FUNCTI,
* 				VARIAB,FORMUL IN PARENS)
* 				AND TAKE THE LAST PRECED VALUE
* 				OFF THE STACK.
* 			2. SEE IF THE NEXT CHARAC IS AN OPERAT
* 				IF NOT, CHECK PREVIO ONE. THIS MAY CAUSE
* 				OPERAT APPLIC OR AN ACTUAL
* 				RETURN FROM "FRMEVL".
* 			3. IF IT IS, SEE WHAT PRECED IT HAS
* 				AND COMPAR IT TO THE PRECED
* 				OF THE LAST OPERAT ON THE STACK.
* 			4. IF = OR LESS REMEMB THE OPERAT
* 				POINTE OF THIS OPERAT
* 				AND BRANCH TO "QCHNUM" TO CAUSE
* 				APPLIC OF THE LAST OPERAT
