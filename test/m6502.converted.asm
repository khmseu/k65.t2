* 			5. IF GREATE PUT THE LAST PRECED
* 				BACK ON, SAVE THE OPERAT ADDRES,
* 				CURREN TEMPOR RESULT,
* 				AND PRECED AND RETURN TO STEP 1.

* 		RELATI OPERAT ARE ALL HANDLE THROUG
* 		A COMMON ROUTIN SPECIA
* 		CARE IS TAKEN TO DETECT TYPE MISMAT SUCH AS 3+"F".

* 	EVAL -- THE ROUTIN TO READ A LEXEME
* 		"EVAL" CHECKS FOR THE DIFFER TYPES OF
* 		ENTITI IT IS SUPPOS TO DETECT
* 		LEADIN PLUSES ARE IGNORE,
* 		DIGITS AND "." CAUSE "FIN" (FLOATI INPUT)
* 		TO BE CALLED FUNCTI NAMES CAUSE THE
* 		FORMUL INSIDE THE PARENT TO BE EVALUA
* 		AND THE FUNCTI ROUTIN TO BE CALLED VARIAB
* 		NAMES CAUSE "PTRGET" TO BE CALLED TO GET A POINTE
* 		TO THE VALUE, AND THEN THE VALUE IS PUT INTO
* 		THE FAC. AN OPEN PARENT CAUSES "FRMEVL"
* 		TO BE CALLED (RECURS), AND THE ")" TO
* 		BE CHECKE FOR. UNARY OPERAT (NOT AND
* 		NEGATI)  PUT THEIR PRECED ON THE STACK
* 		AND ENTER FORMUL EVALUA AT STEP 1, SO
* 		THAT EVERYT UP TO AN OPERAT GREATE THAN
* 		THEIR PRECED OR THE END OF THE FORMUL
